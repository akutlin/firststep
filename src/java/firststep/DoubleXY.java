package firststep;

public class DoubleXY {
	private double x, y;

	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	
	public void setX( double x ) {
		this.x = x;
	}
	public void setY( double y ) {
		this.y = y;
	}
	
	public DoubleXY(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	
}
